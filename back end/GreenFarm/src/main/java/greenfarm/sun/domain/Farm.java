package greenfarm.sun.domain;

public class Farm {

    Integer farmId;
    String farmName;
    String farmSize;
    String farmPrice;
    String farmAddress;
    String farmIntro;
    String farmImg;

    public Integer getFarmId() {
        return farmId;
    }

    public void setFarmId(Integer farmId) {
        this.farmId = farmId;
    }

    public String getFarmName() {
        return farmName;
    }

    public void setFarmName(String farmName) {
        this.farmName = farmName;
    }

    public String getFarmSize() {
        return farmSize;
    }

    public void setFarmSize(String farmSize) {
        this.farmSize = farmSize;
    }

    public String getFarmPrice() {
        return farmPrice;
    }

    public void setFarmPrice(String farmPrice) {
        this.farmPrice = farmPrice;
    }

    public String getFarmAddress() {
        return farmAddress;
    }

    public void setFarmAddress(String farmAddress) {
        this.farmAddress = farmAddress;
    }

    public String getFarmIntro() {
        return farmIntro;
    }

    public void setFarmIntro(String farmIntro) {
        this.farmIntro = farmIntro;
    }

    public String getFarmImg() {
        return farmImg;
    }

    public void setFarmImg(String farmImg) {
        this.farmImg = farmImg;
    }
}
